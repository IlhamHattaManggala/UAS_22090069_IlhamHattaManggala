    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        int unusedID = getUnusedID();
        String nama = txtNama.getText();
        String nip = txtNip.getText();
        String jabatan = txtJabatan.getText();
        String jk = Laki.isSelected() ? "Laki-Laki" : "Perempuan";
        int shift_piket = Pagi.isSelected() ? 1 : 2; // Menggunakan tipe data int
        String no_hp = txtNoHp.getText();
        String alamat = txtAlamat.getText();

        // Validasi input (Anda dapat menambahkan validasi sesuai kebutuhan)
        if (nama.isEmpty() || nip.isEmpty() || jabatan.isEmpty() || no_hp.isEmpty() || alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }

        // Memasukkan data ke dalam database
        try {
            Connection c = Koneksi.sambungkeDB();
            PreparedStatement ps = c.prepareStatement("INSERT INTO data (id, nama, nip, jabatan, jk, shift_piket, no_hp, alamat) VALUES (?, ?, ?, ?, ?, ?, ?, ?)");
            ps.setInt(1, unusedID);
            ps.setString(2, nama);
            ps.setString(3, nip);
            ps.setString(4, jabatan);
            ps.setString(5, jk);
            ps.setInt(6, shift_piket); // Menggunakan tipe data int
            ps.setString(7, no_hp);
            ps.setString(8, alamat);

            int result = ps.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
                // Kembali ke panel data setelah berhasil menyimpan
                mainUtama.removeAll();
                mainUtama.repaint();
                mainUtama.revalidate();
                mainUtama.add(pn_data);
                mainUtama.repaint();
                mainUtama.revalidate();
                // Memuat ulang data di tabel
                ViewData("");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data!");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

